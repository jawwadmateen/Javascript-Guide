//Pre Increment and Post-Increment in JS
let a=10;
console.log(++a); //11  increment then print updated variable
console.log(a++); //11  print previous value and then updates variable
console.log(--a);  //10  //decrements and then prints updated variable
console.log(a--); //10  //prints previous value and then updates variable
console.log(a); //10  
console.log(a--); //10  //prints value and then updates variable